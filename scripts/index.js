const TEXT = `
জাগ্রত হও, নেকড়ের ছেলে যে নিজেকে ভুলে গেছে,
আপনার জাল চেহারা আপনাকে গ্রাস করতে দেবেন না।

নেকড়ে দেবতা নেকড়ের গানের সাথে গাইবেন
নেকড়ে রাস্তা খুলুন এবং হারানো নেকড়েদের বাড়িতে ফিরে যান।

আপনি কি তার কণ্ঠস্বর শুনেছেন?
আপনার সত্যিকারের নিজেকে মনে রাখবেন?

জেগে ওঠো, মিথ্যায় হেরে গেছো নেকড়ে।
`;

const result = () => {
	return TEXT.split('\n')
		.map((line) => {
			return line.split('').reverse().join('');
		})
		.join('\n');
};

console.log(result());
